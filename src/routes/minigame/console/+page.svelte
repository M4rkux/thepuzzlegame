<p>Um console onde devemos digitar a senha para desbloquear o nível</p>
<p>O console terá comandos para dar dicas a senha</p>
<p>A senha será alguma coisa favorita que poderemos listar e mostrar a quantidade de vezes que essa coisa foi suada (ex.: comida) + a idade, onde teremos um outro comando que irá dizer a data de nascimento</p>

<br/>
<p><b>help</b> para mostrar todos os comandos</p>
<p><b>whoami</b> para mostrar os dados do proprietário</p>
<p><b>meals</b> para mostrar as refeições recentes</p>
<p><b>clear</b> para limpar os dados do console</p>
<p><b>solve</b> para resolver o puzzle</p>